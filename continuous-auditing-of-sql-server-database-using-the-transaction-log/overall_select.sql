SELECT A.[LSN]
	,A.[OPERATION_TYPE]
	,A.[OBJECT_NAME]
	,A.[USER_NAME]
	,A.[TRANSACTION_ID]
	,A.[TRANSACTION_BEGIN]
	,A.[TRANSACTION_END]
	,A.[TRANSACTION_DESCRIPTION]
	,A.[ROW_RECONSTRUCTED]
	,A.[PAGE_ID]
	,A.[SLOT_ID]
	,A.[ID_KEY]
	,A.[SPID]
	,A.[SERVER]
	,A.[DATABASE]
	,A.[TRANSACTION_STATE]
	,A.[SCHEMA_NAME]
	,A.[ROW_PARTIALLY_RECONSTRUCTED]
	,A.[ROW_ORDINAL]
	,A.[PARENT_SCHEMA_NAME]
	,A.[PARENT_OBJECT_NAME]
	,A.[PREVIOUS_LSN]
	,A.[DURATION]
	,A.[LOGON_TIME]
	,B.[COLUMN_NAME]
	,B.[COLUMN_TYPE]
	,B.[OLD_VALUE]
	,B.[NEW_VALUE]
FROM [dbo].[APEXSQL_LOG_OPERATION] A
JOIN [dbo].[APEXSQL_LOG_OPERATION_DETAIL] B ON A.LSN = B.LSN